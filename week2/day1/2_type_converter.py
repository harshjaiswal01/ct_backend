#Type conversion functions str, int, float, bool

#Allow us to convert from one datatype to another as long as it is qualifying data

my_str = '5'
print(my_str)
print(type(my_str))

#Converting from string to int
my_int = int(my_str)
print(my_int)
print(type(my_int))

#Converting from int to float

my_float = float(my_int)
print(my_float)
print(type(my_float))

