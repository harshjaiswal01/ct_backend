#Dynamic size means our lists can grow and shrink by adding and removing items

bec_class = ['Harsh', 'Rattana', 'Joshua', 'Isaias']
print(bec_class)

#Adding to Lists

#-- list.append(item) : list method adds an item to the end of the list

bec_class.append("Brian")
print(bec_class)
bec_class.append("Iryna")
print(bec_class)
bec_class.append("Gerardo")
print(bec_class)
bec_class.append("Billy Bob")
print(bec_class)

#Removing from lists

#-- list.remove(item)

bec_class.remove("Gerardo")
print(bec_class)