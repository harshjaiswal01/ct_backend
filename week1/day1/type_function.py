#Type function allows us to see the datatype of any given value

print(type("Hello World"))
print(type(4))
print(type(3.14))

print('4')

#Type casting using int() convert data to int datatype

x = '4'
print(type(x))
print(type(int(x)))

print(int(x))

x = int(x)
print(type(x))

x = float(x)
print(type(x))
print(x)

pi = 3.14
print(pi)

#converting to int always chops off or rounds down
print(int(pi))

